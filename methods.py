def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def sum(a, b):
    result = a + b
    return result

def subtract(a, b):
    result = a - b
    return result

def divide(a, b):
    result = a / b
    return result

def multiply(a, b):
    result = a * b
    return result